#include "fils_de_flut.h"

void	isometrique_projection(t_point *p)
{
	int	tmp;

	tmp = p->x;
	p->x = (tmp - p->y) * cos(0.523599);
	p->y = (tmp + p->y) * sin(0.523599) - p->z;
}
